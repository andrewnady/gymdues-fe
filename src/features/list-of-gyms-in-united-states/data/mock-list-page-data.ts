/** Returns the mock state by code (e.g. "CA") so the state page can show mock count when using mock cities. */
export function getMockStateByCode(stateCode: string): StateWithCount | null {
  const code = stateCode?.trim().toUpperCase()
  if (!code) return null
  return mockStates.find((s) => s.state === code) ?? null
}

/** Returns mock cities for a state. Use when cities API is empty so the state page shows full mock list (e.g. 15 cities for CA). */
export function getMockCitiesForState(stateCode: string, stateName: string): LocationWithCount[] {
  const code = stateCode?.trim().toUpperCase()
  if (!code) return []
  const entries = MOCK_CITIES_BY_STATE[code] ?? [
    { city: stateName, count: Math.max(10, 50) },
  ]
  return entries.map(({ city, count }) => ({
    label: `${city}, ${code}`,
    city,
    state: code,
    postal_code: null as string | null,
    count,
  }))
}
